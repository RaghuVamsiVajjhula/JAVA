
public class NestedLoops 
{
    public static void main(String[] args)
    {
        System.out.println("This is for Nested Loops.");
        //Nested loopes are a loop inside another loop.

        int i,j;
        // int  num[];
        
        for(i=0;i<3;i++)
        {
            for(j=0;j<3;j++)
            {
                System.out.println("Value of i: "+i);
                System.out.println("Value of j: "+j);
            }
        }
       
    }
    
}
